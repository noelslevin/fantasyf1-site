<h1>Rules</h1>
<p>Every game needs rules. They're rarely exciting, but without them, the whole thing wouldn't really work.</p>

<p>The FantasyF1 rules aren't very complicated, but they are here in full to avoid confusion and any end-of-season uncertainty about winners, etc.</p>

<h3>1. Picks</h3>
<ol>
	<li>Players are given 45 points to spend at each race weekend, and can pick as many drivers as they wish within their budget of 45 points.</li>
	<li>Picks must be made by midnight GMT on the Friday morning of the race weekend.</li>
	<li>Players can pick as many times as they want, on the understanding that each set of picks replaces the previous set.</li>
	<li>It is the player's responsibility to check the picks they made are correct and that any errors are reported before the picks deadline (current picks are shown on the picks page).</li>
	<li>Late picks will not be accepted.</li>
	<li>If a driver does not pick, they are deemed not to have entered the race and will not be eligible to score.</li>
	<li>Any driver whose picks total more than 45 points will be subject to arbitrary removal of picks or disqualification from that race.</li>
</ol>

<h3>2. Points</h3>
<ol>
	<li>Scores are calculated in accordance with the FIA Formula 1 World Championship scores, with the exception that full points will always be awarded, even if the race is cut short.</li>
	<li>No other points shall be awarded.</li>
	<li>FantasyF1 entrants will be ranked according to the total points scored in that race.</li>
	<li>Entrants will be ranked from the most points to the least points.</li>
</ol>

<h3>3. Race tie breaks</h3>
<ol>
	<li>If two or more players are tied on points, a tie break situation ensues.</li>
	<li>The tied players will be split according to their picks.</li>
	<li>If one player has selected the race winner, and the other hasn't, they win the tie break.</li>
	<li>If the tied drivers have both picked (or both have not picked) the winning driver, the same process is used for the second-placed driver.</li>
	<li>This continues until the tie break is broken.</li>
	<li>If the tie break is not broken because picks are identical, whoever picked first wins the tie break.</li>
	<li>If a driver does not start the race, according to the official classification, all picks for that driver are void and cannot be used in the tie break procedure.</li>
	<li>Any driver who is disqualified from a race, according to the official classification, will be placed below all starting drivers.</li>
	<li>If more than one driver is disqualified, they will be classified in their finishing order, but below all other starting drivers.</li>
	<li>Disqualified drivers can be used to break tie breaks.</li>
</ol>
	
<h3>4. FantasyF1 Championship</h3>
<ol>
	<li>The winners of the Drivers' FantasyF1 Championship is the player with the most total championship points.</li>
	<li>The winners of the Constructors' FantasyF1 Championship is the team with the most total championship points.</li>
	<li>In the event of a tie in either championship, the entrant with the most overall points will be placed ahead.</li>
</ol>

<h3>5. Prizes</h3>
<ol>
	<li>Prizes are not guaranteed.</li>
	<li>In the event that prizes are awarded, I cannot guarantee that Royal Mail will not steal any prizes sent to Australia, but Norway should be fine.</li>
</ol>

